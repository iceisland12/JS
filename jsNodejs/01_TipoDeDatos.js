/**
    Tipo de datos
 */

// numero - number

console.log(2);
console.log(2.5);

// texto - string
console.log("Luis Guillen");
console.log("Fernado Arrieche");

// Lógicos - booleans
console.log(false);
console.log(true);

// indefinido - undefined
console.log(undefined);

// null
console.log(null);

/**
 * Tipos de datos Estructurales
 */

// object
console.log({
  nombre: "Luis",
  edad: 21,
  fechaDeNacimiento: null,
  esAlto: false,
  cuidades: ["Quito", "Venezuela"],
  dirección: { callePrincipal: "Av este 2" },
});

// arrays (listas)
console.log[("Venezuela", "Colombia")];
console.log([1, true, "Luis", { nombre: "Fernando" }, [1, 2, 3, 4]]);
